"""Regenerate RDKit descriptors from the cleaned CHEMBL379 classification dataset.

This script rebuilds the descriptor matrix from SMILES instead of trusting the raw
exported descriptor values. The output keeps the classification metadata and appends
the full RDKit descriptor surface in the same column order as the original export.
It also records invalid SMILES rows and canonical-SMILES collisions for later review.
"""

import argparse
import csv
import json
from collections import defaultdict
from dataclasses import dataclass
from pathlib import Path

from rdkit import Chem
from rdkit.Chem import Crippen, Descriptors, GraphDescriptors, MolSurf, rdMolDescriptors

from common import find_repo_root, resolve_output_dir, resolve_path, write_csv


SCRIPT_DIR = Path(__file__).resolve().parent
REPO_ROOT = find_repo_root(SCRIPT_DIR)
RAW_DESCRIPTOR_EXPORT_PATH = REPO_ROOT / "datasets" / "CHEMBL379_EC50_AllDesc.csv"
DEFAULT_OUTPUT_DIR_NAME = "rdkit_descriptor_dataset"
DEFAULT_CLASSIFICATION_BASE_DATASET_PATH = (
    SCRIPT_DIR / "classification_curation_dataset" / "classification_base_dataset.csv"
)
RAW_EXPORT_METADATA_COLUMN_COUNT = 7


@dataclass(frozen=True)
class ClassificationBaseRow:
    """Store one row from the clean classification base dataset.

    Attributes:
        smiles: Original SMILES string used for descriptor regeneration.
        activity: Binary activity label.
        threshold_nm: Activity threshold used for labeling.
        source_record_count: Number of source assay rows merged into this row.
        representative_molecule_chembl_id: Representative ChEMBL molecule identifier.
        source_molecule_chembl_ids: Semicolon-separated source molecule IDs.
        source_relations: Semicolon-separated source assay relations.
        source_values_nm: Semicolon-separated source assay values.
        label_sources: Semicolon-separated label provenance values.
        target_chembl_id: ChEMBL target identifier.
    """

    smiles: str
    activity: int
    threshold_nm: float
    source_record_count: int
    representative_molecule_chembl_id: str
    source_molecule_chembl_ids: str
    source_relations: str
    source_values_nm: str
    label_sources: str
    target_chembl_id: str


def _read_classification_rows(dataset_path: Path) -> list[ClassificationBaseRow]:
    """Load the clean classification base dataset.

    Args:
        dataset_path: Path to the CSV generated by build_classification_dataset.py.

    Returns:
        Parsed classification rows.
    """

    rows: list[ClassificationBaseRow] = []
    with dataset_path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        for row in reader:
            rows.append(
                ClassificationBaseRow(
                    smiles=row["smiles"],
                    activity=int(row["activity"]),
                    threshold_nm=float(row["threshold_nm"]),
                    source_record_count=int(row["source_record_count"]),
                    representative_molecule_chembl_id=row[
                        "representative_molecule_chembl_id"
                    ],
                    source_molecule_chembl_ids=row["source_molecule_chembl_ids"],
                    source_relations=row["source_relations"],
                    source_values_nm=row["source_values_nm"],
                    label_sources=row["label_sources"],
                    target_chembl_id=row["target_chembl_id"],
                )
            )
    return rows


def _read_descriptor_column_names(dataset_path: Path) -> list[str]:
    """Read the descriptor column order from the raw export header.

    Args:
        dataset_path: Path to the original descriptor export.

    Returns:
        Descriptor column names in export order.
    """

    with dataset_path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.reader(handle, delimiter="\t")
        header = next(reader)
    return header[RAW_EXPORT_METADATA_COLUMN_COUNT:]


def _compute_stereocenter_counts(molecule: Chem.Mol) -> tuple[int, int]:
    """Compute total and unspecified stereocenter counts for a molecule.

    Args:
        molecule: RDKit molecule.

    Returns:
        Total stereocenter count and unspecified stereocenter count.
    """

    stereocenters = Chem.FindMolChiralCenters(
        molecule,
        includeUnassigned=True,
        includeCIP=False,
        useLegacyImplementation=False,
    )
    unspecified_count = sum(1 for _, label in stereocenters if label == "?")
    return len(stereocenters), unspecified_count


def _prepare_molecule(smiles: str) -> tuple[Chem.Mol, Chem.Mol, str]:
    """Parse a SMILES string and build the RDKit molecule variants.

    Args:
        smiles: SMILES string to featurize.

    Returns:
        The base RDKit molecule, the hydrogen-added molecule, and canonical SMILES.

    Raises:
        ValueError: If RDKit cannot parse the SMILES string.
    """

    molecule = Chem.MolFromSmiles(smiles)
    if molecule is None:
        raise ValueError(f"Invalid SMILES: {smiles}")

    molecule_with_hydrogens = Chem.AddHs(molecule)
    canonical_smiles = Chem.MolToSmiles(molecule, canonical=True, isomericSmiles=True)
    return molecule, molecule_with_hydrogens, canonical_smiles


def _compute_core_descriptor_values(
    molecule: Chem.Mol, molecule_with_hydrogens: Chem.Mol
) -> dict[str, float | int]:
    """Compute the scalar RDKit descriptors used in the export.

    Args:
        molecule: Base RDKit molecule.
        molecule_with_hydrogens: Hydrogen-added RDKit molecule.

    Returns:
        Core scalar descriptor values.
    """

    total_stereocenters, unspecified_stereocenters = _compute_stereocenter_counts(
        molecule_with_hydrogens
    )

    return {
        "SlogP": Crippen.MolLogP(molecule_with_hydrogens),
        "SMR": Crippen.MolMR(molecule_with_hydrogens),
        "LabuteASA": MolSurf.LabuteASA(molecule_with_hydrogens),
        "TPSA": rdMolDescriptors.CalcTPSA(molecule_with_hydrogens),
        "AMW": Descriptors.MolWt(molecule_with_hydrogens),
        "ExactMW": rdMolDescriptors.CalcExactMolWt(molecule_with_hydrogens),
        "NumLipinskiHBA": rdMolDescriptors.CalcNumLipinskiHBA(molecule_with_hydrogens),
        "NumLipinskiHBD": rdMolDescriptors.CalcNumLipinskiHBD(molecule_with_hydrogens),
        "NumRotatableBonds": rdMolDescriptors.CalcNumRotatableBonds(
            molecule,
            rdMolDescriptors.NumRotatableBondsOptions.NonStrict,
        ),
        "NumHBD": rdMolDescriptors.CalcNumHBD(molecule_with_hydrogens),
        "NumHBA": rdMolDescriptors.CalcNumHBA(molecule_with_hydrogens),
        "NumAmideBonds": rdMolDescriptors.CalcNumAmideBonds(molecule_with_hydrogens),
        "NumHeteroAtoms": rdMolDescriptors.CalcNumHeteroatoms(molecule_with_hydrogens),
        "NumHeavyAtoms": molecule_with_hydrogens.GetNumHeavyAtoms(),
        "NumAtoms": molecule_with_hydrogens.GetNumAtoms(),
        "NumStereocenters": total_stereocenters,
        "NumUnspecifiedStereocenters": unspecified_stereocenters,
        "NumRings": rdMolDescriptors.CalcNumRings(molecule_with_hydrogens),
        "NumAromaticRings": rdMolDescriptors.CalcNumAromaticRings(
            molecule_with_hydrogens
        ),
        "NumSaturatedRings": rdMolDescriptors.CalcNumSaturatedRings(
            molecule_with_hydrogens
        ),
        "NumAliphaticRings": rdMolDescriptors.CalcNumAliphaticRings(
            molecule_with_hydrogens
        ),
        "NumAromaticHeterocycles": rdMolDescriptors.CalcNumAromaticHeterocycles(
            molecule_with_hydrogens
        ),
        "NumSaturatedHeterocycles": rdMolDescriptors.CalcNumSaturatedHeterocycles(
            molecule_with_hydrogens
        ),
        "NumAliphaticHeterocycles": rdMolDescriptors.CalcNumAliphaticHeterocycles(
            molecule_with_hydrogens
        ),
        "NumAromaticCarbocycles": rdMolDescriptors.CalcNumAromaticCarbocycles(
            molecule_with_hydrogens
        ),
        "NumSaturatedCarbocycles": rdMolDescriptors.CalcNumSaturatedCarbocycles(
            molecule_with_hydrogens
        ),
        "NumAliphaticCarbocycles": rdMolDescriptors.CalcNumAliphaticCarbocycles(
            molecule_with_hydrogens
        ),
        "FractionCSP3": rdMolDescriptors.CalcFractionCSP3(molecule_with_hydrogens),
        "Chi0v": GraphDescriptors.Chi0v(molecule_with_hydrogens),
        "Chi1v": GraphDescriptors.Chi1v(molecule_with_hydrogens),
        "Chi2v": GraphDescriptors.Chi2v(molecule_with_hydrogens),
        "Chi3v": GraphDescriptors.Chi3v(molecule_with_hydrogens),
        "Chi4v": GraphDescriptors.Chi4v(molecule_with_hydrogens),
        "Chi1n": GraphDescriptors.Chi1n(molecule_with_hydrogens),
        "Chi2n": GraphDescriptors.Chi2n(molecule_with_hydrogens),
        "Chi3n": GraphDescriptors.Chi3n(molecule_with_hydrogens),
        "Chi4n": GraphDescriptors.Chi4n(molecule_with_hydrogens),
        "HallKierAlpha": GraphDescriptors.HallKierAlpha(molecule_with_hydrogens),
        "kappa1": Descriptors.Kappa1(molecule_with_hydrogens),
        "kappa2": Descriptors.Kappa2(molecule_with_hydrogens),
        "kappa3": Descriptors.Kappa3(molecule_with_hydrogens),
    }


def _compute_vsa_descriptor_values(
    molecule_with_hydrogens: Chem.Mol,
) -> dict[str, float]:
    """Compute the RDKit VSA descriptor families.

    Args:
        molecule_with_hydrogens: Hydrogen-added RDKit molecule.

    Returns:
        VSA descriptor values.
    """

    descriptor_values: dict[str, float] = {}

    for index in range(1, 13):
        descriptor_values[f"slogp_VSA{index}"] = getattr(
            MolSurf, f"SlogP_VSA{index}"
        )(molecule_with_hydrogens)

    for index in range(1, 11):
        descriptor_values[f"smr_VSA{index}"] = getattr(MolSurf, f"SMR_VSA{index}")(
            molecule_with_hydrogens
        )

    for index in range(1, 15):
        descriptor_values[f"peoe_VSA{index}"] = getattr(
            MolSurf, f"PEOE_VSA{index}"
        )(molecule_with_hydrogens)

    return descriptor_values


def _compute_mqn_descriptor_values(
    molecule_with_hydrogens: Chem.Mol,
) -> dict[str, int]:
    """Compute the MQN descriptor block.

    Args:
        molecule_with_hydrogens: Hydrogen-added RDKit molecule.

    Returns:
        MQN descriptor values.
    """

    descriptor_values: dict[str, int] = {}
    mqn_values = rdMolDescriptors.MQNs_(molecule_with_hydrogens)
    for index, value in enumerate(mqn_values, start=1):
        descriptor_values[f"MQN{index}"] = value
    return descriptor_values


def _compute_descriptor_values(smiles: str) -> tuple[str, dict[str, float | int]]:
    """Compute RDKit descriptors for one SMILES string.

    The raw export appears to have been generated on hydrogen-added molecules for
    several descriptor families, so this function mirrors that behavior. Rotatable
    bonds use the non-strict RDKit option to match the export convention.

    Args:
        smiles: SMILES string to featurize.

    Returns:
        Canonical SMILES and descriptor dictionary.

    Raises:
        ValueError: If RDKit cannot parse the SMILES string.
    """

    molecule, molecule_with_hydrogens, canonical_smiles = _prepare_molecule(smiles)
    descriptor_values = _compute_core_descriptor_values(
        molecule, molecule_with_hydrogens
    )
    descriptor_values.update(_compute_vsa_descriptor_values(molecule_with_hydrogens))
    descriptor_values.update(_compute_mqn_descriptor_values(molecule_with_hydrogens))

    return canonical_smiles, descriptor_values


def build_rdkit_descriptor_dataset(
    classification_base_dataset_path: Path | None = None,
    output_dir: Path | None = None,
) -> dict[str, int | float | str]:
    """Build the RDKit descriptor dataset from the clean classification base table.

    Args:
        classification_base_dataset_path: Optional path to the classification input.
        output_dir: Optional directory for the generated RDKit outputs.

    Returns:
        Summary statistics for the generated descriptor dataset.
    """

    resolved_classification_base_dataset_path = (
        classification_base_dataset_path or DEFAULT_CLASSIFICATION_BASE_DATASET_PATH
    )
    resolved_output_dir = output_dir or resolve_output_dir(
        SCRIPT_DIR,
        DEFAULT_OUTPUT_DIR_NAME,
    )

    classification_rows = _read_classification_rows(
        resolved_classification_base_dataset_path
    )
    descriptor_column_names = _read_descriptor_column_names(RAW_DESCRIPTOR_EXPORT_PATH)

    descriptor_rows: list[dict[str, object]] = []
    invalid_smiles_rows: list[dict[str, object]] = []
    canonical_groups: dict[str, list[dict[str, object]]] = defaultdict(list)

    for row in classification_rows:
        try:
            canonical_smiles, descriptor_values = _compute_descriptor_values(row.smiles)
        except ValueError:
            invalid_smiles_rows.append(
                {
                    "representative_molecule_chembl_id": row.representative_molecule_chembl_id,
                    "smiles": row.smiles,
                    "activity": row.activity,
                    "exclusion_reason": "invalid_smiles_for_rdkit",
                }
            )
            continue

        missing_descriptors = [
            column_name
            for column_name in descriptor_column_names
            if column_name not in descriptor_values
        ]
        if missing_descriptors:
            raise ValueError(
                "Missing RDKit descriptor mappings for: "
                + ", ".join(missing_descriptors)
            )

        descriptor_row = {
            "representative_molecule_chembl_id": row.representative_molecule_chembl_id,
            "smiles": row.smiles,
            "canonical_smiles": canonical_smiles,
            "activity": row.activity,
            "threshold_nm": row.threshold_nm,
            "source_record_count": row.source_record_count,
            "source_molecule_chembl_ids": row.source_molecule_chembl_ids,
            "source_relations": row.source_relations,
            "source_values_nm": row.source_values_nm,
            "label_sources": row.label_sources,
            "target_chembl_id": row.target_chembl_id,
        }
        for column_name in descriptor_column_names:
            descriptor_row[column_name] = descriptor_values[column_name]
        descriptor_rows.append(descriptor_row)
        canonical_groups[canonical_smiles].append(descriptor_row)

    canonical_collision_rows: list[dict[str, object]] = []
    for canonical_smiles, group in sorted(canonical_groups.items()):
        if len(group) <= 1:
            continue
        canonical_collision_rows.append(
            {
                "canonical_smiles": canonical_smiles,
                "row_count": len(group),
                "representative_molecule_chembl_ids": ";".join(
                    row["representative_molecule_chembl_id"] for row in group
                ),
                "activities": ";".join(str(row["activity"]) for row in group),
                "source_smiles": ";".join(row["smiles"] for row in group),
            }
        )

    resolved_output_dir.mkdir(parents=True, exist_ok=True)
    descriptor_output_path = (
        resolved_output_dir / "classification_rdkit_descriptor_dataset.csv"
    )
    write_csv(
        descriptor_output_path,
        [
            "representative_molecule_chembl_id",
            "smiles",
            "canonical_smiles",
            "activity",
            "threshold_nm",
            "source_record_count",
            "source_molecule_chembl_ids",
            "source_relations",
            "source_values_nm",
            "label_sources",
            "target_chembl_id",
            *descriptor_column_names,
        ],
        descriptor_rows,
    )
    write_csv(
        resolved_output_dir / "rdkit_invalid_smiles.csv",
        [
            "representative_molecule_chembl_id",
            "smiles",
            "activity",
            "exclusion_reason",
        ],
        invalid_smiles_rows,
    )
    write_csv(
        resolved_output_dir / "rdkit_canonical_smiles_collisions.csv",
        [
            "canonical_smiles",
            "row_count",
            "representative_molecule_chembl_ids",
            "activities",
            "source_smiles",
        ],
        canonical_collision_rows,
    )

    summary = {
        "input_classification_dataset": str(
            resolved_classification_base_dataset_path
        ),
        "input_classification_rows": len(classification_rows),
        "descriptor_rows_written": len(descriptor_rows),
        "invalid_smiles_rows": len(invalid_smiles_rows),
        "canonical_collision_groups": len(canonical_collision_rows),
        "descriptor_column_count": len(descriptor_column_names),
        "output_dataset": str(descriptor_output_path),
    }
    with (resolved_output_dir / "rdkit_descriptor_summary.json").open(
        "w", encoding="utf-8"
    ) as handle:
        json.dump(summary, handle, indent=2)

    return summary


def _build_argument_parser() -> argparse.ArgumentParser:
    """Build the command-line interface for RDKit descriptor regeneration."""

    parser = argparse.ArgumentParser(
        description="Regenerate the CHEMBL379 RDKit descriptor dataset."
    )
    parser.add_argument(
        "--classification-dataset",
        help=(
            "Path to classification_base_dataset.csv. Relative paths are resolved "
            "from this script directory."
        ),
    )
    parser.add_argument(
        "--output-dir",
        help=(
            "Directory to write the RDKit dataset and audit files. Relative paths "
            "are resolved from this script directory."
        ),
    )
    parser.add_argument(
        "--suffix",
        help=(
            "Optional suffix appended to the output directory name so a run can "
            "write alongside existing artifacts."
        ),
    )
    return parser


def main() -> None:
    """Run RDKit descriptor regeneration and print the summary."""

    args = _build_argument_parser().parse_args()
    classification_base_dataset_path = (
        None
        if args.classification_dataset is None
        else resolve_path(SCRIPT_DIR, args.classification_dataset)
    )
    output_dir = resolve_output_dir(
        SCRIPT_DIR,
        DEFAULT_OUTPUT_DIR_NAME,
        output_dir=args.output_dir,
        suffix=args.suffix,
    )
    summary = build_rdkit_descriptor_dataset(
        classification_base_dataset_path=classification_base_dataset_path,
        output_dir=output_dir,
    )
    print(json.dumps(summary, indent=2))


if __name__ == "__main__":
    main()